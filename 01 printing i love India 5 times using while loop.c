/// printing i love India 5 times using while loop

#include<stdio.h>
#include<conio.h>

int main()
{
  int cnt = 1;

  printf("\n we are printing i love India 5 times using while loop = \n");

  while(cnt <= 5)
  {

    printf("\n i love India \n");
    cnt++;
  }
  getch();
  return 0;

}
