/// program to print i love India 5 times using do while loop

#include<stdio.h>
#include<conio.h>

int main()
{
  int cnt = 1;

  printf("\n we are printing i love India 5 times using while loop = \n");

  do
  {

    printf("\n i love India \n");
    cnt++;

  } while(cnt <= 5);


  getch();
  return 0;

}
